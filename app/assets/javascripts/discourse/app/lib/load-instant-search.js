/*
Plugins & themes are unable to async-import npm modules directly.
This wrapper provides them with a way to use ember-instantsearch, while keeping the `import()` in core's codebase.
*/
export default async function loadInstantSearch() {
  const [
    AisBaseWidget,
    AisClearRefinements,
    AisCurrentRefinements,
    AisHitsPerPage,
    AisHits,
    AisInfiniteHits,
    AisInstantSearch,
    AisMenuSelect,
    AisMenu,
    AisNumericMenu,
    AisPagination,
    AisPanel,
    AisPoweredBy,
    AisRangeSlider,
    AisRatingMenu,
    AisRefinementList,
    AisRelevantSort,
    AisSearchBox,
    AisSortBy,
    AisStats,
  ] = await Promise.all([
    import("@discourse/ember-instantsearch/components/ais-base-widget"),
    import("@discourse/ember-instantsearch/components/ais-clear-refinements"),
    import("@discourse/ember-instantsearch/components/ais-current-refinements"),
    import("@discourse/ember-instantsearch/components/ais-hits-per-page"),
    import("@discourse/ember-instantsearch/components/ais-hits"),
    import("@discourse/ember-instantsearch/components/ais-infinite-hits"),
    import("@discourse/ember-instantsearch/components/ais-instant-search"),
    import("@discourse/ember-instantsearch/components/ais-menu-select"),
    import("@discourse/ember-instantsearch/components/ais-menu"),
    import("@discourse/ember-instantsearch/components/ais-numeric-menu"),
    import("@discourse/ember-instantsearch/components/ais-pagination"),
    import("@discourse/ember-instantsearch/components/ais-panel"),
    import("@discourse/ember-instantsearch/components/ais-powered-by"),
    import("@discourse/ember-instantsearch/components/ais-range-slider"),
    import("@discourse/ember-instantsearch/components/ais-rating-menu"),
    import("@discourse/ember-instantsearch/components/ais-refinement-list"),
    import("@discourse/ember-instantsearch/components/ais-relevant-sort"),
    import("@discourse/ember-instantsearch/components/ais-search-box"),
    import("@discourse/ember-instantsearch/components/ais-sort-by"),
    import("@discourse/ember-instantsearch/components/ais-stats"),
  ]);

  return {
    AisBaseWidget: AisBaseWidget.default,
    AisClearRefinements: AisClearRefinements.default,
    AisCurrentRefinements: AisCurrentRefinements.default,
    AisHitsPerPage: AisHitsPerPage.default,
    AisHits: AisHits.default,
    AisInfiniteHits: AisInfiniteHits.default,
    AisInstantSearch: AisInstantSearch.default,
    AisMenuSelect: AisMenuSelect.default,
    AisMenu: AisMenu.default,
    AisNumericMenu: AisNumericMenu.default,
    AisPagination: AisPagination.default,
    AisPanel: AisPanel.default,
    AisPoweredBy: AisPoweredBy.default,
    AisRangeSlider: AisRangeSlider.default,
    AisRatingMenu: AisRatingMenu.default,
    AisRefinementList: AisRefinementList.default,
    AisRelevantSort: AisRelevantSort.default,
    AisSearchBox: AisSearchBox.default,
    AisSortBy: AisSortBy.default,
    AisStats: AisStats.default,
  };
}
